#include<bits/stdc++.h>
using namespace std;

int main()
{
    int n=5;

    while (cin>> n)
    {
            if(n==1)
            cout<< "A"<<endl;

            else if(n==2)
            cout<< "B" <<endl;

            else if(n==3)
            cout<< "C" <<endl;

            else if(n==4)
            cout<< "D" <<endl;

            else if(n==5)
            cout<< "E" <<endl;
    }
    
        


    return 0;
}